"""
Утилиты для анализа текстов поездок.

Содержит функции для обнаружения намёков на дни, бюджет и даты в тексте.
"""
from __future__ import annotations

import re


def has_days_hint(text: str) -> bool:
    """
    Проверяет, есть ли в тексте указание на количество дней.
    
    Примеры: "5 дней", "3 дня", "7 суток", "4 ночи"
    """
    return bool(
        re.search(
            r"\b\d{1,2}\s*(?:-|–|—|до)?\s*\d{0,2}\s*(?:дн(?:я|ей)?|сут(?:ок)?|ноч(?:ь|и|ей)?)",
            text,
            flags=re.IGNORECASE,
        )
    )


def has_budget_hint(text: str) -> bool:
    """
    Проверяет, есть ли в тексте указание на бюджет.
    
    Примеры: "бюджет 50000", "эконом", "до 30000", "без ограничений"
    """
    lowered = text.lower()
    return any(
        keyword in lowered
        for keyword in (
            "бюджет",
            "эконом",
            "дешев",
            "дёшев",
            "подешевле",
            "средн",
            "комфорт",
            "бизнес",
            "первый класс",
            "не ограничен",
            "без ограничений",
            "любой бюджет",
            "премиум",
            "люкс",
            "до ",
            "от ",
            "на ",
            "около ",
        )
    ) or bool(re.search(r"\b\d[\d\s]{3,}\b", lowered))


def has_dates_hint(text: str) -> bool:
    """
    Проверяет, есть ли в тексте указание на даты.
    
    Примеры: "12-16 июня", "12.06", "12 июня"
    """
    MONTHS_RU = (
        "январ", "феврал", "март", "апрел", "мая", "май",
        "июн", "июл", "август", "сентябр", "октябр", "ноябр", "декабр",
    )
    lowered = text.lower()
    return any(
        keyword in lowered
        for keyword in MONTHS_RU
    ) or bool(re.search(r"\b\d{1,2}\s*(?:-|–|—|до)\s*\d{1,2}\b", text))
